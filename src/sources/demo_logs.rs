//! demo_logs source - A simple example of a polling source.
//!
//! # Purpose
//!
//! The `demo_logs` source generates fake log events for testing and demos.
//! It's an excellent reference for understanding how to build a source because:
//! - It's simple enough to read in one sitting
//! - It demonstrates the polling pattern (periodically generate data)
//! - It shows proper shutdown handling
//! - It demonstrates decoder integration
//!
//! # How It Works
//!
//! 1. Configuration is parsed from TOML into `DemoLogsConfig`
//! 2. `build()` creates a decoder and spawns the source task
//! 3. The source task loops, generating logs at the configured interval
//! 4. Each log is decoded into an Event and sent through SourceSender
//! 5. The loop exits when shutdown is signaled or count is reached
//!
//! # Key Patterns Demonstrated
//!
//! - **Polling**: The source wakes up periodically to generate data
//! - **Shutdown handling**: Uses `futures::poll!` to check shutdown without blocking
//! - **Decoder integration**: Generated strings are decoded into Events
//! - **Metadata enrichment**: Source adds standard metadata (timestamp, source_type, etc.)
//! - **Telemetry**: Emits internal events for bytes/events received

